
We live in a world where tracking individuals via technology has become a tool not only for advertisers, but also for governments and security services.

We at the Aurelius Fund believe this activity is dystopian, no matter who does it.

Security services should obtain warrants that are hard to get before eavesdropping. Citizens deserve to be free from the evils that follow any state agent's unbridled access to our private lives.

Private data brokerage, honestly, should not exist. Surveillance advertising is creepy and makes using technology joyless. There's more honorable ways to make money. Consumers deserve privacy when in using any commercial product.

Please support legislation to enforce privacy, our God-given human right, acknowledged in Article 12 of the Universal Declaration of Human Rights, in commercial and governmental activities.

